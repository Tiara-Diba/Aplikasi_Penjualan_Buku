    private void btnPilihBarangActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPilihBarangActionPerformed
        // TODO add your handling code here:
         if (btnPilihBarang.getSelectedItem().equals("pilih buku")){
            txtKodeBuku.setText("");
            txtHargaSatuan.setText("");
        }else{
        try {
            java.sql.Statement stat = conn.createStatement();
            String sql = "SELECT kode_buku, jumlah_buku FROM tb_kategori WHERE judul_buku ='" + btnPilihBarang.getSelectedItem() + "'";
            ResultSet hasil = stat.executeQuery(sql);

            while (hasil.next()) {
                txtKodeBuku.setText(hasil.getString("kode_buku"));
                txtJumlah.setText(hasil.getString("jumlah_buku"));
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
        try {
            java.sql.Statement stat = conn.createStatement();

            String sql = "SELECT harga_buku FROM tb_kategori WHERE judul_buku ='" + btnPilihBarang.getSelectedItem() + "'";
            ResultSet hasil = stat.executeQuery(sql);

            while (hasil.next()) {
                txtHargaSatuan.setText(hasil.getString("harga_buku"));
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
         }
    }//GEN-LAST:event_btnPilihBarangActionPerformed

