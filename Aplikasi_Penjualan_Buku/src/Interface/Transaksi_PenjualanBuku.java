    private void btnHitungActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHitungActionPerformed
        // TODO add your handling code here:
        if(txtKodeBuku.getText().equals("") || btnPilihBarang.getSelectedItem().equals("")|| txtHargaSatuan.getText().equals("")|| txtJumlahBeli.getText().equals("")){
            JOptionPane.showMessageDialog(null, "LENGKAPI DATA !", "BOOK STORE", JOptionPane.INFORMATION_MESSAGE);

        }else{
            String a = txtJumlahBeli.getText();
            int aa = Integer.parseInt(a);

            String b = txtJumlah.getText();
            int bb = Integer.parseInt(b);
            if(aa > bb){
                JOptionPane.showMessageDialog(null, "jumlah melebihi stok", "BOOK STORE", JOptionPane.INFORMATION_MESSAGE);
                txtJumlahBeli.setText("");
            }else{

                if(txtJumlahBeli.getText().equals("")){
                    JOptionPane.showMessageDialog(null, "ISI JUMLAH BELI!");
                }else{
                    int jumlah, harga, total;

                    jumlah = Integer.parseInt(txtJumlahBeli.getText().toString());
                    harga = Integer.parseInt(txtHargaSatuan.getText().toString());
                    total = jumlah * harga;

                    txtTotal.setText(Integer.toString(total));

                }
            }
        }

    }//GEN-LAST:event_btnHitungActionPerformed

    private void btnTambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTambahActionPerformed
        // TODO add your handling code here:

        if(txtKodeBuku.getText().equals("") || btnPilihBarang.getSelectedItem().equals("")|| txtHargaSatuan.getText().equals("")|| txtJumlahBeli.getText().equals("")|| txtTotal.getText().equals("")){
            JOptionPane.showMessageDialog(null, "LENGKAPI DATA!", "BOOK STORE", JOptionPane.INFORMATION_MESSAGE);

        }else{
            String kodebuku = txtKodeBuku.getText();
            String pilihbarang = (String)btnPilihBarang.getSelectedItem();
            String stokbuku = txtJumlah.getText();
            String hargasatuan = txtHargaSatuan.getText();
            String jumlahbeli = txtJumlahBeli.getText();
            String total = txtTotal.getText();

            try {
                java.sql.Statement stat = conn.createStatement();
                String sql = "INSERT INTO tb_tmp_beli VALUES (?, ?, ?, ?, ?, ?)";
                PreparedStatement hasil = conn.prepareStatement(sql);
                hasil.setString(1, pilihbarang);
                hasil.setString(2, stokbuku);
                hasil.setString(3, kodebuku);
                hasil.setString(4, hargasatuan);
                hasil.setString(5, jumlahbeli);
                hasil.setString(6, total);

                hasil.executeUpdate();
                hasil.close();
            } catch (SQLException e) {
                System.out.println("Terjadi Error");
            } finally {
                txtKodeBuku.setText("");
                btnPilihBarang.setSelectedItem("");
                txtHargaSatuan.setText("");
                txtJumlahBeli.setText("");
                txtJumlah.setText("");
                txtTotal.setText("");
                JOptionPane.showMessageDialog(null, "Data berhasil disimpan", "BOOK STORE", JOptionPane.INFORMATION_MESSAGE);
                loadData();

            }
        }
    }//GEN-LAST:event_btnTambahActionPerformed

